if x > 10:
    print("x more than ten")
    print("ไอ้บาสขี้เย็ด")
else :
    print('บาสไม่เย็ด')

print("ถ้าอันนี้คือออกจาก if แล้ว")

#Comparison Operators
x = int(input("Enter the number :"))
if x == 5 :
    print('Equals 5')
if x > 4 :
    print('Greater than 4')
if x >= 5 :
    print('Greater than or Equals 5')
if x < 6 :
    print('Less than 6')
if x <= 5 :
    print('Less than or Equals 5')
if x != 6 :
    print('Not equal 6')

# Nested Decisions
x = 42
if x > 1 :
    print('More than one')
    if x < 100 :
        print('Less than 100')
print('All Done')

#Two-way Decissions with else
x = int(input("Enter the number : "))
if x > 2 :
    print('Bigger')
else :
    print('Smaller')
print('All done')

